static HD_RESULT set_proc_cfg(HD_PATH_ID *p_video_proc_ctrl, HD_DIM* p_max_dim)
{
	HD_RESULT ret = HD_OK;
	HD_VIDEOPROC_DEV_CONFIG video_cfg_param = {0};
	HD_VIDEOPROC_CTRL video_ctrl_param = {0};
	HD_PATH_ID video_proc_ctrl = 0;

	ret = hd_videoproc_open(0, HD_VIDEOPROC_0_CTRL, &video_proc_ctrl); //open this for device control
	if (ret != HD_OK)
		return ret;

	if (p_max_dim != NULL ) {
		video_cfg_param.pipe = 0xf2;//HD_VIDEOPROC_PIPE_VPE
		if (g_effect == VPE_EFFECT_NONE) {
			video_cfg_param.isp_id = HD_ISP_DONT_CARE;
		} else {
			video_cfg_param.isp_id = ISP_EFFECT_ID;
		}
		video_cfg_param.ctrl_max.func = 0;
		video_cfg_param.in_max.func = 0;
		video_cfg_param.in_max.dim.w = p_max_dim->w;
		video_cfg_param.in_max.dim.h = p_max_dim->h;
		video_cfg_param.in_max.pxlfmt = HD_VIDEO_PXLFMT_YUV420;
		video_cfg_param.in_max.frc = HD_VIDEO_FRC_RATIO(1,1);
		ret = hd_videoproc_set(video_proc_ctrl, HD_VIDEOPROC_PARAM_DEV_CONFIG, &video_cfg_param);
		if (ret != HD_OK) {
			return HD_ERR_NG;
		}
	}
	{
		HD_VIDEOPROC_FUNC_CONFIG video_path_param = {0};
		video_path_param.in_func = 0;
		video_path_param.ddr_id = VDO_DDR_ID;
		ret = hd_videoproc_set(video_proc_ctrl, HD_VIDEOPROC_PARAM_FUNC_CONFIG, &video_path_param);
	}

	video_ctrl_param.func = 0;
	ret = hd_videoproc_set(video_proc_ctrl, HD_VIDEOPROC_PARAM_CTRL, &video_ctrl_param);

	*p_video_proc_ctrl = video_proc_ctrl;

	return ret;
}

static HD_RESULT set_proc_param(VIDEO_PROCESS *p_stream)
{
	HD_RESULT ret = HD_OK;
	UINT32 i;

	for (i=0; i < OUT_VPE_COUNT; i++) {
		p_stream->proc0_out[i].func = 0;
		p_stream->proc0_out[i].dim.w = VDO_SIZE_W;
		p_stream->proc0_out[i].dim.h = VDO_SIZE_H;
		p_stream->proc0_out[i].pxlfmt = HD_VIDEO_PXLFMT_YUV420;
		p_stream->proc0_out[i].dir = HD_VIDEO_DIR_NONE;
		p_stream->proc0_out[i].frc = HD_VIDEO_FRC_RATIO(1,1);
		p_stream->proc0_out[i].depth = 1; //set 1 to allow pull
		if (user_out_buf) {
			p_stream->proc0_out[i].bg.w = VDO_SIZE_W;
			p_stream->proc0_out[i].bg.h = VDO_SIZE_H;
		}
		ret = hd_videoproc_set(p_stream->proc0_path[i], HD_VIDEOPROC_PARAM_OUT, &p_stream->proc0_out[i]);
		if (ret)
			return ret;

		{
			HD_VIDEOPROC_FUNC_CONFIG video_path_param = {0};
			video_path_param.out_func = 0;
			video_path_param.ddr_id = VDO_DDR_ID;
			ret = hd_videoproc_set(p_stream->proc0_path[i], HD_VIDEOPROC_PARAM_FUNC_CONFIG, &video_path_param);
		}
	}


	return ret;
}


