static HD_RESULT open_module(VIDEO_PROCESS *p_stream, HD_DIM* p_proc_max_dim)
{
    HD_RESULT ret;
	UINT32 i;

	// set videoproc config
	ret = set_proc_cfg(&p_stream->proc0_ctrl, p_proc_max_dim);
	if (ret != HD_OK) {
		printf("set proc-cfg fail=%d\n", ret);
		return HD_ERR_NG;
	}

	if (g_effect) {
		if ((ret = set_effect_cfg()) != HD_OK)
			return ret;
	}

	for (i=0; i < OUT_VPE_COUNT; i++) {
		if ((ret = hd_videoproc_open(HD_VIDEOPROC_0_IN_0, HD_VIDEOPROC_OUT(0, i), &p_stream->proc0_path[i])) != HD_OK)
			return ret;
	}
	return HD_OK;
}

static HD_RESULT close_module(VIDEO_PROCESS *p_stream)
{
	HD_RESULT ret;
	UINT32 i;

	for (i = 0; i < OUT_VPE_COUNT; i++) {
		if ((ret = hd_videoproc_close(p_stream->proc0_path[i])) != HD_OK)
			return ret;
	}
	if (g_effect) {
		vendor_vpe_uninit();
	}
	return HD_OK;
}

