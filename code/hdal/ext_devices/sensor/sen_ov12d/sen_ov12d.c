static void sen_set_flip_ov12d(CTL_SEN_ID id, CTL_SEN_FLIP *flip)
{
	CTL_SEN_CMD cmd;
	ER rt = E_OK;
	UINT32 start_x = 0, start_y = 0, temp = 0;

	if (!first_rote_flag[id]) {
		cmd = sen_set_cmd_info_ov12d(0x3811, 1, 0x0, 0x0);
		rt |= sen_read_reg_ov12d(id, &cmd);
		start_x_ori[id] = cmd.data[0];

		cmd = sen_set_cmd_info_ov12d(0x3813, 1, 0x0, 0x0);
		rt |= sen_read_reg_ov12d(id, &cmd);
		start_y_ori[id] = cmd.data[0];
		first_rote_flag[id] = 1;
	}

	cmd = sen_set_cmd_info_ov12d(0x3821, 1, 0x0, 0x0);
	rt |= sen_read_reg_ov12d(id, &cmd);

	if (basic_param.property & CTL_SEN_SUPPORT_PROPERTY_MIRROR) {
		if (*flip & CTL_SEN_FLIP_H) {
				cmd.data[0] &= (~0x04);
				temp = cmd.data[0];
				start_x = start_x_ori[id] - 1;
		} else {
				cmd.data[0] |= 0x4;
				temp = cmd.data[0];
				start_x = start_x_ori[id];
		}
	} else {
		DBG_WRN("no support mirror \r\n");
	}

	cmd = sen_set_cmd_info_ov12d(0x0100, 1, 0x00, 0x0);
	rt |= sen_write_reg_ov12d(id, &cmd);

	cmd = sen_set_cmd_info_ov12d(0x3821, 1, temp, 0x0);
	rt |= sen_write_reg_ov12d(id, &cmd);

	cmd = sen_set_cmd_info_ov12d(0x3811, 1, start_x, 0x0);
	rt |= sen_write_reg_ov12d(id, &cmd);

	cmd = sen_set_cmd_info_ov12d(0x3820, 1, 0x0, 0x0);
	rt |= sen_read_reg_ov12d(id, &cmd);

	if (basic_param.property & CTL_SEN_SUPPORT_PROPERTY_FLIP) {
		if (*flip & CTL_SEN_FLIP_V) {
			cmd.data[0] |= 0x4;
			temp = cmd.data[0];
			start_y = start_y_ori[id] - 1;	
		} else {
			cmd.data[0] &= (~0x04);
			temp = cmd.data[0];
			start_y = start_y_ori[id];		
		}
	} else {
		DBG_WRN("no support flip \r\n");
	}

	cmd = sen_set_cmd_info_ov12d(0x3820, 1, temp, 0x0);
	rt |= sen_write_reg_ov12d(id, &cmd);

	cmd = sen_set_cmd_info_ov12d(0x3813, 1, start_y, 0x0);
	rt |= sen_write_reg_ov12d(id, &cmd);

	cmd = sen_set_cmd_info_ov12d(0x0100, 1, 0x01, 0x0);
	rt |= sen_write_reg_ov12d(id, &cmd);

	if (rt != E_OK) {
		DBG_ERR("write register error %d \r\n", (INT)rt);
	}
}

static ER sen_get_flip_ov12d(CTL_SEN_ID id, CTL_SEN_FLIP *flip)
{
	CTL_SEN_CMD cmd;
	ER rt = E_OK;

	*flip = CTL_SEN_FLIP_NONE;

	cmd = sen_set_cmd_info_ov12d(0x3821, 1, 0x0, 0x0);
	rt |= sen_read_reg_ov12d(id, &cmd);
	if (!(cmd.data[0] & (0x4))) {
		*flip |= CTL_SEN_FLIP_H;
	}

	cmd = sen_set_cmd_info_ov12d(0x3820, 1, 0x0, 0x0);
	rt |= sen_read_reg_ov12d(id, &cmd);
	if (cmd.data[0] & 0x4) {
		*flip |= CTL_SEN_FLIP_V;
	}

	return rt;
}

